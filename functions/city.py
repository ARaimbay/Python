def describe_city(name_city='Reykjavik', country_city='Iceland'):
    """Defining city by name and country"""
    print(f"{name_city.title()} is in {country_city.title()}.")
describe_city()
describe_city('almaty', 'kazakhstan')
describe_city(name_city='chicago', country_city='the united states of america')
describe_city('capetown', 'south africa')
