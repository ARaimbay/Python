def make_shirt(shirt_size='L', shirt_sign='I love Python'):
    """Making a custom shirt"""
    print(f"The size of a shirt is: {shirt_size.title()}.")
    print(f"The sign on the shirt is: {shirt_sign}.")
make_shirt('l', 'You are beautiful')
make_shirt(shirt_size='L', shirt_sign='Universe is yours')
make_shirt()
make_shirt(shirt_size='M')
